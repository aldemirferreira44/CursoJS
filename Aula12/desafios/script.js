var agora = new Date()
var hora = agora.getHours
if (hora <= 12) {
    document.write(`Bom Dia. São ${hora} hora(s)`)
    style.backgroundColor; yellow
} else if (hora < 18) {
    document.write(`Boa Tarde. São ${hora} hora(s)`)
    style.backgroundColor; brown
} else {
    document.write(`Boa Noite. São ${hora} hora(s)`)
    style.backgroundColor; black;
}
